package info.san.books.app.command.saga;

public class SagaUpdateCommand {

	private String id;

	private String nom;

	public SagaUpdateCommand(String id, String nom) {
		this.id = id;
		this.nom = nom;
	}

	public String getId() {
		return this.id;
	}

	public String getNom() {
		return this.nom;
	}

}
