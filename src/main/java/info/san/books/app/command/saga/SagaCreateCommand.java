package info.san.books.app.command.saga;

import java.util.UUID;

public class SagaCreateCommand {

	private String id;

	private String nom;

	public SagaCreateCommand(String nom) {
		this.id = UUID.randomUUID().toString();
		this.nom = nom;
	}

	public String getId() {
		return this.id;
	}

	public String getNom() {
		return this.nom;
	}

}
