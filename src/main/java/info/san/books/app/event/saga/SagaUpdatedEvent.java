package info.san.books.app.event.saga;

import java.io.Serializable;

public class SagaUpdatedEvent implements Serializable {

	private static final long serialVersionUID = -8760902671618353471L;

	private String id;

	private String nom;

	public SagaUpdatedEvent(String id, String nom) {
		this.id = id;
		this.nom = nom;
	}

	public String getId() {
		return this.id;
	}

	public String getNom() {
		return this.nom;
	}

}
